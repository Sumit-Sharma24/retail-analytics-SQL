-- creating a new database
create database retail_analytics_db;

use retail_analytics_db;

show tables;

select * from customer_profiles;
select * from product_inventory;
select * from sales_transaction;

-- finding duplicate transactions
select
transactionid, count(*) as occurence
from sales_transaction
group by transactionid
having occurence > 1;

-- craeting new table with unique transactions
create table sales_transaction_copy as
select
distinct *
from sales_transaction;

-- remove original sales_transaction
drop table if exists sales_transaction;

rename table sales_transaction_copy to sales_transaction;

-- identify the discrepancies in the price
select
t.transactionid,
t.price as transaction_price,
p.price as product_price
from sales_transaction as t
join product_inventory as p
on t.productid = p.productid
where t.price <> p.price;

SET SQL_SAFE_UPDATES = 0;

-- update the discrepancies
update sales_transaction as t
join product_inventory as p
on t.productid = p.productid
set t.price = p.price
where t.price <> p.price;

SET SQL_SAFE_UPDATES = 1;

-- identify empty values
select
count(*)
from customer_profiles
where customerid = ''
or age = ''
or gender = ''
or location = ''
or joindate = '';

-- replace empty value with nulls
update customer_profiles
