where location = '';

-- identify null values in customer data
select
count(*)
from customer_profiles
where customerid is null
or age is null
or gender is null
or location is null
or joindate is null;

-- replace null with unknown in customer data
update customer_profiles
set age = ifnull(age, 'Unknown'),
customerid = ifnull(customerid, 'Unknown'),
gender = ifnull(gender, 'Unknown'),
location = ifnull(location, 'Unknown'),
joindate = ifnull(joindate, 'Unknown');

-- changing transactiondate from text to date
create table sales_transaction_updated as
select
*,
str_to_date(transactiondate, '%d-%m-%Y') as TransactionDate_updated
from sales_transaction;

drop table if exists sales_transaction;

rename table sales_transaction_updated to sales_transaction;

alter table sales_transaction
drop column transactiondate;

alter table sales_transaction
rename column TransactionDate_updated to TransactionDate;

-- finding total sales and quantity sold
select
productid as ProductID,
sum(quantitypurchased) as TotalUnitsSold,
round(sum(quantitypurchased * price), 2) as TotalSales
from sales_transaction
group by productid
order by TotalSales desc;

-- transactions per customer
select
CustomerID, count(transactionid) as NumberOfTransactions
from sales_transaction
group by CustomerID
order by NumberOfTransactions desc;

-- performance of product category
select
p.Category,
sum(s.quantitypurchased) as TotalUnitsSold,
round(sum(s.quantitypurchased * s.price), 2) as TotalSales
from sales_transaction as s
join product_inventory as p
on s.productid = p.productid
group by p.Category
order by TotalSales desc;

-- top 10 products with highest sales
select
ProductID,
round(sum(quantitypurchased * price), 2) as TotalRevenue
from sales_transaction
group by ProductID
order by TotalRevenue desc
limit 10;

