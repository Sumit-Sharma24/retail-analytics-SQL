-- finding sales trends
select
TransactionDate,
count(transactionid) as Transaction_count,
round(sum(quantitypurchased), 2) as TotalUnitsSold,
round(sum(quantitypurchased * price), 2) as TotalSales
from sales_transaction
group by TransactionDate
order by TransactionDate asc;

-- monthwise growth rate
with sales_total as (
    select
    month(transactiondate) as month,
    round(sum(quantitypurchased * price), 2) as total_sales
    from sales_transaction
    group by month
    order by month asc
)
select
*,
lag(total_sales) over(order by month) as previous_month_sales,
round(((total_sales - lag(total_sales) over(order by month)) / lag(total_sales) over(order by month)) * 100, 2)
as mom_growth_percentage
from sales_total;

-- number of transaction along with the total amount spent by each customer
select
CustomerID,
count(transactionid) as NumberOfTransactions,
round(sum(quantitypurchased * price), 2) as TotalSpent
from sales_transaction
group by CustomerID
having NumberOfTransactions > 10 and TotalSpent > 1000
order by TotalSpent desc;

-- low purchase frequency in the company
select
CustomerID,
count(transactionid) as NumberOfTransactions,
sum(quantitypurchased * price) as TotalSpent
from sales_transaction
group by CustomerID
having NumberOfTransactions <= 2
order by NumberOfTransactions asc,
TotalSpent desc;

-- repeat purchase
select
CustomerID,
ProductID,
count(ProductID) as TimesPurchased
from sales_transaction
group by CustomerID,
ProductID
having TimesPurchased > 1
order by TimesPurchased desc;

-- duration between the first and the last purchase of the customer -> Loyality Indicators
SELECT
CustomerID,
MIN(cast(transactiondate as date)) AS FirstPurchase,
MAX(cast(transactiondate as date)) AS LastPurchase,
DATEDIFF(MAX(cast(transactiondate as date)), MIN(cast(transactiondate as date))) AS DaysBetweenPurchases
FROM sales_transaction
GROUP BY CustomerID
HAVING DaysBetweenPurchases > 0
ORDER BY DaysBetweenPurchases DESC;

-- Customer Segmentation
with segment as(
    select
    c.customerid,
    case
        when sum(quantitypurchased) between 1 and 10 then 'Low'
        when sum(quantitypurchased) between 11 and 30 then 'Med'
        else 'High'
    end as CustomerSegment
    from customer_profiles as c
    join sales_transaction as s
    on c.customerid = s.customerid
    group by c.customerid
)
select
CustomerSegment,
count(*)
from segment
group by CustomerSegment;

